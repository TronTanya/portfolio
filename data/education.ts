export type EducationCredentialType =
  | "higher"
  | "secondary_vocational"
  | "additional"
  | "professional_course"

export type EducationEntry = {
  id: string
  type: EducationCredentialType
  institution: string
  degree: string
  field: string
  years: string
  description: string
  keySubjects: string[]
  /** Навыки и компетенции, выросшие из программы */
  skills: string[]
  /** Скан диплома в `public/` (например `/documents/diploma.pdf`). */
  diplomaPdfUrl?: string
  /** Превью (первая страница или фото), рядом с кнопкой PDF — `public/assets/education/`. */
  diplomaImageUrl?: string
  /** Подпись для превью (доступность). */
  diplomaImageAlt?: string
  /** Подпись кнопки скачивания; по умолчанию — «Диплом (PDF)». */
  diplomaLabel?: string
  /** Серия, номер, рег. № — одной строкой для карточки. */
  diplomaRequisites?: string
}

